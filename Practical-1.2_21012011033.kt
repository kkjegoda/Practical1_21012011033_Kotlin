fun main(){
    var i:Int=8
    var num1:Double=i.toDouble()
    println("Integer variable:$i")
    println("Integer to Double Coversion:$num1")
    var s:String="24.63"
    var num2:Double=s.toDouble()
    println("String to Double Conversion:$num2")
    var s1:String="33"
    var num3:Int=s1.toInt()
    println("String to Integer Coversion:$num3")



}